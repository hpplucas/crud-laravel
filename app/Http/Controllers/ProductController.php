    {
        var_dump($request->all());
        $prod = new Product();
        $prod->name = $request['name'];
        $prod->stock = $request['stock'];
        $prod->price = $request['price'];
        $prod->category_id = $request['category'];
        $prod->save();

        return redirect()->route('products.index');
